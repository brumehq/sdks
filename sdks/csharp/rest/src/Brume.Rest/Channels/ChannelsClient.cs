    /// <summary>
    /// Returns a list of all channels for the authenticated project.
    /// </summary>
    /// <example><code>
    /// await client.Channels.ListChannelsAsync();
    /// </code></example>
    public WithRawResponseTask<ChannelListResponse> ListChannelsAsync(
        RequestOptions? options = null,
        CancellationToken cancellationToken = default
    )
    {
        return new WithRawResponseTask<ChannelListResponse>(
            ListChannelsAsyncCore(options, cancellationToken)
        );
    }

